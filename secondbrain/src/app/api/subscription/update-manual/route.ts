import { NextRequest, NextResponse } from 'next/server';
import { updateUserSubscription } from '@/lib/subscription-operations';

export async function POST(req: NextRequest) {
  try {
    const { userId, planType } = await req.json();

    if (!userId || !planType) {
      return NextResponse.json(
        { error: 'Missing userId or planType' },
        { status: 400 }
      );
    }

    if (!['basic', 'pro', 'elite'].includes(planType)) {
      return NextResponse.json(
        { error: 'Invalid plan type' },
        { status: 400 }
      );
    }

    console.log('üîß [Manual Update] Actualizando suscripci√≥n manualmente:', { userId, planType });

    await updateUserSubscription(userId, {
      plan: planType as 'basic' | 'pro' | 'elite',
      status: 'active',
      // No incluimos datos de Stripe ya que es una actualizaci√≥n manual
    });

    console.log('‚úÖ [Manual Update] Suscripci√≥n actualizada manualmente');

    return NextResponse.json({ success: true, message: 'Suscripci√≥n actualizada correctamente' });
  } catch (error) {
    console.error('‚ùå [Manual Update] Error actualizando suscripci√≥n:', error);
    return NextResponse.json(
      { error: 'Error updating subscription' },
      { status: 500 }
    );
  }
}
